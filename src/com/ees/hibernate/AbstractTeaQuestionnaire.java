package com.ees.hibernate;

/**
 * AbstractTeaQuestionnaire entity provides the base persistence definition of
 * the TeaQuestionnaire entity. @author MyEclipse Persistence Tools
 */

public abstract class AbstractTeaQuestionnaire implements java.io.Serializable {

	// Fields

	private Integer qid;
	private String content;
	private Integer weight;

	// Constructors

	/** default constructor */
	public AbstractTeaQuestionnaire() {
	}

	/** minimal constructor */
	public AbstractTeaQuestionnaire(Integer qid, String content) {
		this.qid = qid;
		this.content = content;
	}

	/** full constructor */
	public AbstractTeaQuestionnaire(Integer qid, String content, Integer weight) {
		this.qid = qid;
		this.content = content;
		this.weight = weight;
	}

	// Property accessors

	public Integer getQid() {
		return this.qid;
	}

	public void setQid(Integer qid) {
		this.qid = qid;
	}

	public String getContent() {
		return this.content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getWeight() {
		return this.weight;
	}

	public void setWeight(Integer weight) {
		this.weight = weight;
	}

}